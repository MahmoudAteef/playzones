        <script>
            // ═══════════════════════════════════════════════════════════
            // Lightbox Functionality for FAQ Images
            // ═══════════════════════════════════════════════════════════
            document.addEventListener('DOMContentLoaded', function() {
                const lightboxModal = document.getElementById('lightboxModal');
                const lightboxImage = document.getElementById('lightboxImage');
                const lightboxClose = document.querySelector('.lightbox-close');

                // البحث عن جميع الصور داخل محتوى الأسئلة
                const faqImages = document.querySelectorAll('.faq-content img');

                faqImages.forEach(img => {
                    img.addEventListener('click', function() {
                        lightboxImage.src = this.src;
                        lightboxImage.alt = this.alt || 'صورة توضيحية';
                        lightboxModal.classList.add('active');
                        document.body.style.overflow = 'hidden';
                        // منع التمرير
                    });
                }
                );

                // إغلاق الـ Lightbox
                function closeLightbox() {
                    lightboxModal.classList.remove('active');
                    document.body.style.overflow = '';
                    // إعادة التمرير
                }

                if (lightboxClose) {
                    lightboxClose.addEventListener('click', closeLightbox);
                }

                // إغلاق عند الضغط خارج الصورة
                if (lightboxModal) {
                    lightboxModal.addEventListener('click', function(e) {
                        if (e.target === lightboxModal) {
                            closeLightbox();
                        }
                    });
                }

                // إغلاق عند الضغط على ESC
                document.addEventListener('keydown', function(e) {
                    if (e.key === 'Escape' && lightboxModal && lightboxModal.classList.contains('active')) {
                        closeLightbox();
                    }
                });
            });
        </script>
